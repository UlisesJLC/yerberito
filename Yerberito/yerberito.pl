% Regla para encontrar plantas que tratan un síntoma específico
trata_sintoma(Sintoma, Planta) :-
    planta(Planta, Sintoma).

% Regla para encontrar todas las formas de usar una planta
forma_uso(Planta, Forma) :-
    plantas_preparacion(Planta, Formas),
    member(Forma, Formas).

% ¿Cuáles son plantas o plantas medicinales?
plantas_medicinales(ListaPlantas) :-
    findall(Planta, planta(Planta, _), ListaPlantasRepetidas),
    list_to_set(ListaPlantasRepetidas, ListaPlantas).

% ¿Qué elementos se encuentran en las plantas?
elementos_en_plantas(ListaElementos) :-
    findall(Elemento, (
        planta(Planta, _),
        componente_quimico(Planta, Elementos),
        member(Elemento, Elementos)
    ), ElementosRepetidos),
    list_to_set(ElementosRepetidos, ListaElementos).

% ¿Qué plantas producen medicamentos?
plantas_que_producen_medicamentos(ListaPlantas) :-
    findall(Planta, medicamento(Planta, _), ListaPlantas).

% ¿Qué medicamentos provienen de plantas?
medicamentos_de_plantas(ListaMedicamentos) :-
    findall(Medicamento, medicamento(_, Medicamento), ListaMedicamentos).

% ¿Cuáles son las acciones o efectos de medicamentos provenientes de plantas?
acciones_efectos_medicamentos(ListaAcciones) :-
    findall(Accion, accion(_, Accion), ListaAcciones).

% ¿Cuáles son las acciones o efectos que tienen las plantas?
acciones_efectos_plantas(ListaAcciones) :-
    findall(Accion, (medicamento(_, Medicamento), accion(Medicamento, Accion)), ListaAcciones).

% Listado de plantas y sus acciones o efectos sobre el organismo
plantas_y_sus_acciones(Lista) :-
    findall((Planta, Accion), (medicamento(Planta, Medicamento), accion(Medicamento, Accion)), Lista).

% ¿Qué plantas son analgésicas?
plantas_analgesicas(ListaPlantas) :-
    findall(Planta, (medicamento(Planta, Medicamento), accion(Medicamento, analgesico)), ListaPlantas).

% Listar plantas medicinales y su nombre científico
nombre_cientifico_lista(Lista) :-
    findall((Planta, NombreCientifico), nombre_cientifico(Planta, NombreCientifico), Lista).

% ¿Cuáles son las enfermedades que curan las plantas?
enfermedades_que_curan_las_plantas(Lista) :-
    findall((Planta, Enfermedad), planta(Planta, Enfermedad), ListaRepetida),
    list_to_set(ListaRepetida, Lista).

% ¿Cuáles son las formas de preparación para tratamiento de enfermedades con uso de plantas?
preparacion_para_enfermedades(Lista) :-
    findall((Planta, Forma), forma_uso(Planta, Forma), Lista).

% ¿Cuáles son los orígenes de las plantas medicinales?
origen_de_plantas_medicinales(Lista) :-
    findall((Planta, Origen), origen(Planta, Origen), Lista).

% ¿Qué elementos tiene una planta en específica?
elementos_planta_especifica(Planta, ListaElementos) :-
    componente_quimico(Planta, Elementos),
    list_to_set(Elementos, ListaElementos).

% ¿Qué medicamentos produce una planta en específica?
medicamentos_planta(Planta, Medicamento) :-
    medicamento(Planta, Medicamento).

% ¿Cuáles son los efectos o acciones de un medicamento en específico?
