% Botiquín de plantas
botiquin(ListaPlantas) :-
    findall(Planta, planta(Planta, _), PlantasRepetidas),
    list_to_set(PlantasRepetidas, Plantas),
    random_permutation(Plantas, Permutacion),
    length(Botiquin, 5),
    append(Botiquin, _, Permutacion),
    ListaPlantas = Botiquin.